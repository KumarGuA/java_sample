package ControlStatements.PriyankaAssessments;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a = 4;
		int var;

		// assign value using =
		var = a;
		System.out.println("fetch Value of var=:" + var);

		// assign value using +=

		var += a;
		System.out.println("fetch value of var+=:" + var);

		// assign value using

		var *= a;
		System.out.println("fetch value of var*=:" + var);

	}

}
